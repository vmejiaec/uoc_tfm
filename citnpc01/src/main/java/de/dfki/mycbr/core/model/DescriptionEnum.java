package de.dfki.mycbr.core.model;

/**
 * Lists the possible attribute descriptions to model the basic vocabulary of
 * this project.
 * 
 * @author myCBR Team
 * 
 */
public enum DescriptionEnum {
	Boolean("Boolean"), Concept("Concept"), Date("Date"), Interval("Interval"), 
	Integer("Integer"), Float("Float"), Double("Double"), String("String"), Symbol("Symbol");

	private String name;

	DescriptionEnum(String name) {
		this.name = name;
	}

	public String getName() {
		return name;
	}
}
